package com.example.agendor_timeline.model;

public class Urls {

    private String urlemail;
    private String urloutros;
    private String urlcall;
    private String urlvisita;
    private String urlreuniao;
    private String urlproposta;

    public String getUrlemail() {
        return urlemail;
    }

    public void setUrlemail(String urlemail) {
        this.urlemail = urlemail;
    }

    public String getUrloutros() {
        return urloutros;
    }

    public void setUrloutros(String urloutros) {
        this.urloutros = urloutros;
    }

    public String getUrlcall() {
        return urlcall;
    }

    public void setUrlcall(String urlcall) {
        this.urlcall = urlcall;
    }

    public String getUrlvisita() {
        return urlvisita;
    }

    public void setUrlvisita(String urlvisita) {
        this.urlvisita = urlvisita;
    }

    public String getUrlreuniao() {
        return urlreuniao;
    }

    public void setUrlreuniao(String urlreuniao) {
        this.urlreuniao = urlreuniao;
    }

    public String getUrlproposta() {
        return urlproposta;
    }

    public void setUrlproposta(String urlproposta) {
        this.urlproposta = urlproposta;
    }
}
